#include "libft.h"

void	ft_toupperstr(char **str)
{
	int i;

	i = 0;
	while (str && str[0] && str[0][i])
	{
		if (str[0][i] >= 'a' && str[0][i] <= 'z')
			str[0][i] = ft_toupper(str[0][i]);
		i++;
	}
}

#include "libft.h"

void	ft_strrev(char **str)
{
	int		i;
	int		l;

	i = 0;
	while (i < (l = ft_strlen(*str)) / 2)
	{
		ft_swap(&(*str)[i], &(*str)[l - i - 1]);
		i++;
	}
}

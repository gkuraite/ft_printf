#include "libft.h"

void	ft_putnbr(int n)
{
	long int i;

	i = 1;
	if (n < 0)
	{
		ft_putchar('-');
		i = -i;
	}
	while ((n / i) >= 10)
		i = 10 * i;
	while (i != 0)
	{
		ft_putchar(n / i + 48);
		n = n % i;
		i = i / 10;
	}
}

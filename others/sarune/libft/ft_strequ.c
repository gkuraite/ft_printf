#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int		i;

	i = 0;
	if (s1 == NULL || s2 == NULL)
		return (0);
	while (*s1 != '\0' || *s2 != '\0')
	{
		i = *s1 - *s2;
		if (i != 0)
			return (0);
		s1++;
		s2++;
	}
	return (1);
}

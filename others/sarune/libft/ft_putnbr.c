#include "libft.h"

void	ft_putnbr(int n)
{
	long int	i;
	char		str;

	i = n;
	if (n < 0)
	{
		ft_putchar('-');
		i = -i;
	}
	str = i % 10 + '0';
	if (i > 9)
		ft_putnbr(i / 10);
	ft_putchar(str);
}
